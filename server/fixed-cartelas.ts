// Helper function to get fixed cartela pattern
export function getFixedCartelaPattern(cartelaNum: number): number[][] {
  const cartela = FIXED_CARTELAS.find(c => c.Board === cartelaNum);
  if (!cartela) {
    throw new Error(`Cartela ${cartelaNum} not found in fixed cartelas`);
  }

  const card: number[][] = [[], [], [], [], []];
  
  // Convert cartela data to 5x5 grid format
  for (let row = 0; row < 5; row++) {
    for (let col = 0; col < 5; col++) {
      if (col === 0) {
        card[row].push(cartela.B[row]);
      } else if (col === 1) {
        card[row].push(cartela.I[row]);
      } else if (col === 2) {
        if (row === 2) {
          card[row].push(0); // FREE space
        } else {
          card[row].push(cartela.N[row] as number);
        }
      } else if (col === 3) {
        card[row].push(cartela.G[row]);
      } else if (col === 4) {
        card[row].push(cartela.O[row]);
      }
    }
  }
  
  return card;
}

// Helper function to get all numbers from a cartela (excluding FREE)
export function getCartelaNumbers(cartelaNum: number): number[] {
  const cartela = FIXED_CARTELAS.find(c => c.Board === cartelaNum);
  if (!cartela) {
    throw new Error(`Cartela ${cartelaNum} not found`);
  }

  const numbers: number[] = [];
  
  // Add B numbers
  cartela.B.forEach((num: number) => numbers.push(num));
  
  // Add I numbers
  cartela.I.forEach((num: number) => numbers.push(num));
  
  // Add N numbers (excluding FREE)
  cartela.N.forEach((num: number | string) => {
    if (num !== "FREE") {
      numbers.push(num as number);
    }
  });
  
  // Add G numbers
  cartela.G.forEach((num: number) => numbers.push(num));
  
  // Add O numbers
  cartela.O.forEach((num: number) => numbers.push(num));
  
  return numbers;
}